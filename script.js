var sound1 = new Audio();
sound1.src = "/Task-Audios/Audios/ambulance sound.wav"

var PlayPauseBTN1 = document.getElementById('PlayPauseBTN1');
var count = 0;
function playpause1() {
    if (count == 0) {
        count = 1;
        sound1.play();
    } else {
        count = 0;
        sound1.pause();
    }
}

